#include "ft_printf.h"

void	ft_putnbr(int n, int *c)
{
	if (n < 0)
	{
		ft_putchar('-');
		++*c;
		if (n == -2147483648)
		{
			ft_putchar('2');
			++*c;
			n = -147483648;
		}
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_putnbr((n / 10), c);
		ft_putnbr((n % 10), c);
	}
	else
	{
		ft_putchar(n + 48);
		++*c;
	}
}
